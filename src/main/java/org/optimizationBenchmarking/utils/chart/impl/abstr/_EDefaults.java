package org.optimizationBenchmarking.utils.chart.impl.abstr;

/** An enum of the default values */
enum _EDefaults {

  /** the chart title font */
  CHART_TITLE_FONT,
  /** the data title font */
  DATA_TITLE_FONT,
  /** the data stroke */
  DATA_STROKE,
  /** the axis title font */
  AXIS_TITLE_FONT,
  /** the chart title font */
  AXIS_TICK_FONT,
  /** the axis stroke */
  AXIS_STROKE,
  /** the axis color */
  AXIS_COLOR,
  /** the grid stroke */
  GRID_STROKE,
  /** the grid color */
  GRID_COLOR;

  /** the number of defaults */
  static final int DEFAULT_COUNT = _EDefaults.values().length;
}
